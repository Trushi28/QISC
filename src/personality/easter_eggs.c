char *easter_encode_secret(const char *message, const char *method) {
  if (!message)
    return NULL;

  size_t len = strlen(message);
  char *encoded = malloc(len + 1);
  if (!encoded)
    return NULL;

  if (!method || strcmp(method, "rot13") == 0) {
    /* ROT13 encoding */
    for (size_t i = 0; i < len; i++) {
      char c = message[i];
      if (c >= 'a' && c <= 'z') {
        encoded[i] = ((c - 'a' + 13) % 26) + 'a';
      } else if (c >= 'A' && c <= 'Z') {
        encoded[i] = ((c - 'A' + 13) % 26) + 'A';
      } else {
        encoded[i] = c;
      }
    }
  } else if (strcmp(method, "reverse") == 0) {
    /* Reverse string */
    for (size_t i = 0; i < len; i++) {
      encoded[i] = message[len - 1 - i];
    }
  } else if (strcmp(method, "hex") == 0) {
    /* Hex encoding - needs larger buffer */
    free(encoded);
    encoded = malloc(len * 2 + 1);
    if (!encoded)
      return NULL;
    for (size_t i = 0; i < len; i++) {
      sprintf(encoded + i * 2, "%02x", (unsigned char)message[i]);
    }
    encoded[len * 2] = '\0';
    return encoded;
  } else {
    /* Unknown method, just copy */
    strcpy(encoded, message);
  }

  encoded[len] = '\0';
  return encoded;
}

char *easter_embed_ascii_art(const char *art_name, size_t *out_size) {
  const char *art = NULL;

  if (!art_name || strcmp(art_name, "logo") == 0) {
    art = binary_ascii_art_logo;
  } else if (strcmp(art_name, "minimal") == 0) {
    art = binary_ascii_art_minimal;
  } else if (strcmp(art_name, "replicant") == 0) {
    art = binary_ascii_art_replicant;
  } else {
    art = binary_ascii_art_minimal;
  }

  size_t len = strlen(art);
  char *result = malloc(len + 1);
  if (!result)
    return NULL;

  memcpy(result, art, len + 1);
  if (out_size)
    *out_size = len;

  return result;
}

