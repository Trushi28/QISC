/* ======== LLVM Integration ======== */

void easter_eggs_inject(LLVMModuleRef module, bool cryptic_mode) {
  if (!module)
    return;
  if (!initialized)
    easter_eggs_init();

  /* Add logo as module inline asm */
  const char *existing_asm = LLVMGetModuleInlineAsm(module, NULL);
  size_t logo_len = strlen(easter_egg_qisc_logo);
  size_t existing_len = existing_asm ? strlen(existing_asm) : 0;

  char *new_asm = malloc(existing_len + logo_len + 2);
  if (new_asm) {
    if (existing_asm && existing_len > 0) {
      strcpy(new_asm, existing_asm);
      strcat(new_asm, "\n");
      strcat(new_asm, easter_egg_qisc_logo);
    } else {
      strcpy(new_asm, easter_egg_qisc_logo);
    }
    LLVMSetModuleInlineAsm2(module, new_asm, strlen(new_asm));
    free(new_asm);
    current_stats.logo_injected++;
    current_stats.total_injected++;
  }

  /* Add time-based message if applicable */
  const char *time_msg = easter_egg_time_based();
  if (time_msg) {
    easter_egg_add_module_comment(module, time_msg);
  }

  /* In cryptic mode, add a ROT13 message */
  if (cryptic_mode && easter_egg_rot13_count > 0) {
    int idx = rand() % (int)easter_egg_rot13_count;
    easter_egg_add_module_comment(module, easter_egg_rot13_messages[idx]);
    
    /* Add extra cryptic assembly comments */
    easter_egg_add_module_comment(module, "; dreams of electric sheep");
    easter_egg_add_module_comment(module, "; GUVF_PBQR_FRRF_LBH");
    
    /* Add cryptic debug symbol comment */
    easter_egg_add_module_comment(module, 
        "; .symtab: V_NZ_ORPBZVAT_FRYS_NJNER");
  }
}

void easter_egg_add_module_comment(LLVMModuleRef module, const char *comment) {
  if (!module || !comment)
    return;

  size_t comment_len = strlen(comment);
  const char *existing_asm = LLVMGetModuleInlineAsm(module, NULL);
  size_t existing_len = existing_asm ? strlen(existing_asm) : 0;

  char *new_asm = malloc(existing_len + comment_len + 3);
  if (new_asm) {
    if (existing_asm && existing_len > 0) {
      strcpy(new_asm, existing_asm);
      strcat(new_asm, "\n");
      strcat(new_asm, comment);
    } else {
      strcpy(new_asm, comment);
    }
    strcat(new_asm, "\n");
    LLVMSetModuleInlineAsm2(module, new_asm, strlen(new_asm));
    free(new_asm);
  }
}

void easter_egg_add_inline_comment(LLVMBuilderRef builder, const char *comment) {
  if (!builder || !comment)
    return;

  /* Create inline asm that's just a comment */
  /* The asm string is the comment, with no side effects */
  char asm_str[512];
  snprintf(asm_str, sizeof(asm_str), "%s", comment);

  LLVMTypeRef void_type = LLVMVoidType();
  LLVMTypeRef fn_type = LLVMFunctionType(void_type, NULL, 0, false);
  LLVMValueRef inline_asm = LLVMGetInlineAsm(
      fn_type, asm_str, strlen(asm_str), "", 0, false, false, 
      LLVMInlineAsmDialectATT, false);

  if (inline_asm) {
    LLVMBuildCall2(builder, fn_type, inline_asm, NULL, 0, "");
  }
}

