const EasterEgg *easter_eggs_get_builtins(size_t *count) {
  if (count) {
    size_t n = 0;
    while (builtin_eggs[n].trigger != NULL)
      n++;
    *count = n;
  }
  return builtin_eggs;
}

bool easter_eggs_enabled(void) {
  /* Check environment variable */
  const char *env = getenv("QISC_EASTER_EGGS");
  if (env) {
    if (strcmp(env, "0") == 0 || strcmp(env, "off") == 0 ||
        strcmp(env, "false") == 0) {
      return false;
    }
  }
  return true;
}

